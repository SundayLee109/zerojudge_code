#include <iostream>
using namespace std;
int main(){
  int n,sum;
  while(cin>>n){
    sum=0;
    if(n>40){
      n=40;
    }
    for(int i=1;i<=n;i++){
      if(n==0){
        break;
      }else if(i>=1&&i<=10){
        sum=sum+6;
      }else if(i>=11&&i<=20){
        sum=sum+2;
      }else{
        sum=sum+1;
      }
    }
    cout<<sum<<endl;
  }
}
